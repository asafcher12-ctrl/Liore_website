// Breadcrumb helper
export const getBreadcrumbs = (path) => {
  const breadcrumbs = [
    { name: 'בית', url: '/' },
  ]

  if (path === '/') return breadcrumbs

  if (path.includes('/blog')) {
    breadcrumbs.push({ name: 'בלוג', url: '/blog' })

    if (path.includes('/sleep-alone')) {
      breadcrumbs.push({ name: 'למה הילד שלי לא מוכן לישון לבד?', url: '/blog/sleep-alone' })
    } else if (path.includes('/parental-anger')) {
      breadcrumbs.push({ name: 'כעס הורי', url: '/blog/parental-anger' })
    } else if (path.includes('/connection')) {
      breadcrumbs.push({ name: 'חמש דקות חיבור', url: '/blog/connection' })
    } else if (path.includes('/age-three')) {
      breadcrumbs.push({ name: 'גיל 3', url: '/blog/age-three' })
    }
  } else if (path === '/about') {
    breadcrumbs.push({ name: 'אודות', url: '/about' })
  } else if (path === '/services') {
    breadcrumbs.push({ name: 'שירותים', url: '/services' })
  } else if (path === '/sleep') {
    breadcrumbs.push({ name: 'ייעוץ שינה', url: '/sleep' })
  } else if (path === '/contact') {
    breadcrumbs.push({ name: 'צרי קשר', url: '/contact' })
  }

  return breadcrumbs
}

